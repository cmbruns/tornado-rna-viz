    private static Map<String, Color> nameColors = new HashMap<String, Color>();
    private static Map<Color, Set<String>> colorNames = new HashMap<Color, Set<String>>();
    private static Set<Color> allColors = new HashSet<Color>();
    
    private ColorName() {} // No constructor - only static methods
    
    protected static Color addColor(String colorName, String colorString) {
        Color color = Color.getColor(colorString);

        nameColors.put(colorName, color);
        if (! colorNames.containsKey(color)) colorNames.put(color, new HashSet<String>());
        colorNames.get(color).add(colorName);
        colorNames.get(color).add(canonicalNameString(colorName));
        allColors.add(color);

        return color;
    }
    
    public static Color getColor(String colorName) {
        Color answer = nameColors.get(colorName);
        if (answer == null) answer = nameColors.get(canonicalNameString(colorName));
        return answer;
    }
    
    public static String getName(Color color) {
        // TODO - create another method that returns names NEAR this color
        Set<String> names = colorNames.get(color);
        if (names == null) return null;
        return names.iterator().next();
    }
    
    public static Set<String> getNames(Color color) {
        Set<String> names = colorNames.get(color);
        return names;
    }

    private static String canonicalNameString(String name) {
        return name.toUpperCase().replaceAll("\\s", "");
    }

